print(7 // 4)       # а
print(7 % 4)        # б
print(-7 // 4)      # в
print(-7 % 4)       # г
print(7 // 4)       # д
print(7 % -4)       # е
print(-7 // -4)     # ж
print(-7 % -4)      # з
print(7 // -4)      # доп

